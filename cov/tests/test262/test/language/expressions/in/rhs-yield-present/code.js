function * isNameIn() {
  return '' in (yield);
}

let iter1 = isNameIn();
iter1.next();
assert.sameValue(iter1.next({'': 0}).value, true);

let iter2 = isNameIn();
iter2.next();
assert.sameValue(iter2.next({}).value, false);

